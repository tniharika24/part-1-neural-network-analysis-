ax[1].plot(history.history['accuracy'], label='train')
ax[1].plot(history.history['val_accuracy'], label='val')
ax[1].set_title("Accuracy")
ax[1].set_xlabel("epoch")
ax[1].legend()

plt.tight_layout()
