# ## Task 6 — Reflection

# **1. Role of weights and biases**

# The weights basically decide how much each feature matters when moving from
# one layer to the next. Biases let the neuron shift its output up or down
# even when the input is 0 — so the model has more flexibility. During
# backpropagation these are the things that actually get updated to reduce
# the loss.

# **2. Why we need an activation function**

# If we don't put any activation function, then no matter how many layers we
# stack, the whole network just becomes a fancy linear regression. ReLU /
# sigmoid / tanh add non-linearity which is what lets the network learn
# complex patterns like the kind of customer who is likely to churn.

# **3. Learning rate effects**

# - Too high (like my Exp3 with lr=0.01): the loss kept jumping around and
#   the model never really settled. AUC came out lower.
# - Too low: training would be slow and might stop before learning enough.
# - 0.001 with Adam felt like a sweet spot for this dataset.

# **4. Overfitting or underfitting?**

# In my baseline (Exp1) the train and test accuracy were close, so it
# generalized okay. The deeper network in Exp2 had higher train accuracy but
# test didn't improve much — slight overfitting. Exp3 with high LR was just
# unstable. Overall the baseline gave the best balance.

# Since the data is imbalanced, I trusted the AUC score and the recall for
# the churn class more than plain accuracy.

