## Task 5 — Hyperparameter Experiments

##Trying 5 different settings — changing depth, learning rate, activation,and batch size to see how each affects results.

experiments = [
    {'name': 'Exp1_Baseline',       'hidden': (32, 16),      'lr': 0.001, 'bs': 32,  'epochs': 50, 'act': 'relu'},
    {'name': 'Exp2_DeeperNet',      'hidden': (64, 32, 16),  'lr': 0.001, 'bs': 32,  'epochs': 50, 'act': 'relu'},
    {'name': 'Exp3_HighLR',         'hidden': (32, 16),      'lr': 0.01,  'bs': 32,  'epochs': 50, 'act': 'relu'},
    {'name': 'Exp4_TanhActivation', 'hidden': (32, 16),      'lr': 0.001, 'bs': 32,  'epochs': 50, 'act': 'tanh'},
    {'name': 'Exp5_LargeBatch',     'hidden': (32, 16),      'lr': 0.001, 'bs': 128, 'epochs': 50, 'act': 'relu'},
]

results = []

for e in experiments:
    print(f"Running {e['name']} ...")
    # reset seed each time so it's fair
    tf.random.set_seed(42)
    np.random.seed(42)

    m = build_model(input_dim,
                    hidden_layers=e['hidden'],
                    activation=e['act'],
                    learning_rate=e['lr'])

    m.fit(X_train_sc, y_train,
          validation_split=0.2,
          epochs=e['epochs'],
          batch_size=e['bs'],
          class_weight=class_weights,
          verbose=0)

    tr_acc = m.evaluate(X_train_sc, y_train, verbose=0)[1]
    te_acc = m.evaluate(X_test_sc,  y_test,  verbose=0)[1]
    auc = roc_auc_score(y_test, m.predict(X_test_sc, verbose=0).flatten())

    results.append({
        'Experiment'   : e['name'],
        'Hidden Layers': str(e['hidden']),
        'Activation'   : e['act'],
        'Learning Rate': e['lr'],
        'Batch Size'   : e['bs'],
        'Epochs'       : e['epochs'],
        'Train Acc'    : round(tr_acc, 4),
        'Test Acc'     : round(te_acc, 4),
        'Test AUC'     : round(auc, 4),
    })

comp_df = pd.DataFrame(results)
